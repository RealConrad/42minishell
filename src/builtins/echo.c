#include "minishell.h"

void	echo(char *str)
{
	char	*end;
	
	str += 8;
	if (*str == '"')
		str++;
	end = str + ft_strlen(str) - 2;
	if ((*end == '\'' || *end == '"'))
		*end = '\0';
	printf("%s", str);
}
